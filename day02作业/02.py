chengJi = int(input(print("请输入成绩")))
if chengJi == 0:
   print("鸡蛋")    
elif chengJi > 0 and chengJi <60:
   print("不合格")  
elif chengJi >=60 and chengJi <70:
   print("合格")
elif chengJi >=70 and chengJi <80:
   print("还可以多努力")
elif chengJi >= 80 and chengJi <90:
   print("良好")
elif chengJi >=90 and chengJi < 100:
   print("优秀")
elif chengJi == 100:
   print("满分")
